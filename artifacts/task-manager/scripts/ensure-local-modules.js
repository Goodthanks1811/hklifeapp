/**
 * 1. Symlinks local native modules into node_modules/ so Metro and the Xcode
 *    build-phase autolinking can find them (pnpm hoists file: deps to the
 *    workspace root, so they don't land in the project's own node_modules).
 *
 * 2. Patches expo-configure-project.sh to call patch-expo-modules-provider.js
 *    immediately after autolinking regenerates ExpoModulesProvider.swift.
 *    This injects AppleMusicKitModule.self into the provider so ExpoModulesCore
 *    registers the module at runtime — bypassing the autolinking gap caused by
 *    pnpm hoisting.
 *
 * Runs as both `postinstall` (npm lifecycle, before prebuild) and
 * `eas-build-post-install` (after pod install, when the shell script exists).
 */

const fs   = require("fs");
const path = require("path");

const PROJECT_ROOT = path.resolve(__dirname, "..");

// ─── 1. Symlink local modules ────────────────────────────────────────────────

const LOCAL_MODULES = ["apple-musickit"];

function copyRecursive(src, dst) {
  fs.mkdirSync(dst, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name);
    const dstPath = path.join(dst, entry.name);
    if (entry.isDirectory()) {
      copyRecursive(srcPath, dstPath);
    } else {
      fs.copyFileSync(srcPath, dstPath);
    }
  }
}

for (const name of LOCAL_MODULES) {
  const src = path.join(PROJECT_ROOT, "modules", name);
  const dst = path.join(PROJECT_ROOT, "node_modules", name);

  if (!fs.existsSync(src)) {
    console.warn(`[ensure-local-modules] Source not found: ${src} — skipping`);
    continue;
  }

  try {
    const stat = fs.lstatSync(dst);
    if (stat.isSymbolicLink() || stat.isDirectory()) {
      fs.rmSync(dst, { recursive: true, force: true });
    }
  } catch {
    // dst doesn't exist — nothing to remove
  }

  try {
    fs.symlinkSync(src, dst, "dir");
    console.log(`[ensure-local-modules] Symlinked ${name} → node_modules/`);
    continue;
  } catch {
    // Symlink not supported — fall through to copy
  }

  try {
    copyRecursive(src, dst);
    console.log(`[ensure-local-modules] Copied ${name} → node_modules/ (recursive)`);
  } catch (e) {
    console.error(`[ensure-local-modules] Failed to copy ${name}:`, e);
    process.exit(1);
  }
}

// ─── 2. Patch expo-configure-project.sh ─────────────────────────────────────
//
// expo-configure-project.sh is the [Expo] Configure project Xcode build-phase
// script generated by `use_expo_modules!` during pod install. It calls the
// expo-modules-autolinking Node.js tool which WRITES ExpoModulesProvider.swift
// at runtime ($SCRIPT_OUTPUT_FILE_0).
//
// We append a call to patch-expo-modules-provider.js at the END so it runs
// immediately after autolinking writes the file — before Swift compilation.
//
// $PROJECT_DIR (set by Xcode) = ios/  →  $PROJECT_DIR/../scripts/ = scripts/

const CONFIGURE_SCRIPT = path.join(
  PROJECT_ROOT,
  "ios",
  "Pods",
  "Target Support Files",
  "Pods-HKLifeApp",
  "expo-configure-project.sh"
);

if (!fs.existsSync(CONFIGURE_SCRIPT)) {
  console.log("[ensure-local-modules] expo-configure-project.sh not found — skipping patch (expected on postinstall; only available after pod install)");
} else {
  let content = fs.readFileSync(CONFIGURE_SCRIPT, "utf8");

  if (content.includes("patch-expo-modules-provider")) {
    console.log("[ensure-local-modules] expo-configure-project.sh already patched — skipping");
  } else {
    const hook = `\n# --- Injected by ensure-local-modules.js (eas-build-post-install) ---\nnode "$PROJECT_DIR/../scripts/patch-expo-modules-provider.js"\n`;
    fs.writeFileSync(CONFIGURE_SCRIPT, content + hook, "utf8");
    console.log("[ensure-local-modules] ✅ Patched expo-configure-project.sh with AppleMusicKit hook");
  }
}
